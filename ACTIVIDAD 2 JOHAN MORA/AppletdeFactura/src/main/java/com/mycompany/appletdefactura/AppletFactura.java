    private void Boton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Boton1MouseClicked
        DecimalFormat formato = new DecimalFormat("#,###");//

        String name = NameClient.getText();
        
        int valorp1 = Integer.parseInt(ValorP1.getText());
        int valorp2 = Integer.parseInt(ValorP2.getText());
        int valorp3 = Integer.parseInt(ValorP3.getText());
        
        int cantidadp1 = Integer.parseInt(CantidadP1.getText());
        int cantidadp2 = Integer.parseInt(CantidadP2.getText());
        int cantidadp3 = Integer.parseInt(CantidadP3.getText());
        
        int vtotalp1,vtotalp2, vtotalp3;
        
        vtotalp1 = valorp1*cantidadp1;
        vtotalp2 = valorp2*cantidadp2;
        vtotalp3 = valorp3*cantidadp3;
        
        int total;
        
        total=vtotalp1 + vtotalp2 + vtotalp3;
        String TotalDecimal = formato.format(total);
        lbl_Final.setText("Se√±or@ "+name+" el total de su compra es: $"+TotalDecimal);
        
        TotalP1.setText(Integer.toString(vtotalp1));
        TotalP2.setText(Integer.toString(vtotalp2));
        TotalP3.setText(Integer.toString(vtotalp3));
        

        TotalCompra.setText(TotalDecimal);

        

    }//GEN-LAST:event_Boton1MouseClicked

